package LearnMorePractice2;

public class PhanNguyenVaPhanDu {

	public static void du(String[] args) {

		int divid = 20;

		int divis = 3;

		int quotient = divid / divis;

		int remainder = divid % divis;

		System.out.println("Phần nguyên khi chia " + divid + " cho " + divis + " là: " + quotient);

		System.out.println("Phần dư khi chia " + divid + " cho " + divis + " là: " + remainder);

	}

}